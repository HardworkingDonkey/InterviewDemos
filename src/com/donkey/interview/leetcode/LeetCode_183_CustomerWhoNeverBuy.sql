-- Customers 表:
-- +----+-------+
-- | Id | Name  |
-- +----+-------+
-- | 1  | Joe   |
-- | 2  | Henry |
-- | 3  | Sam   |
-- | 4  | Max   |
-- +----+-------+
-- Orders 表:
-- +----+------------+
-- | Id | CustomerId |
-- +----+------------+
-- | 1  | 3          |
-- | 2  | 1          |
-- +----+------------+

-- 找出从不订购的客户
SELECT Name Customers
FROM Customers
WHERE Id NOT IN (
    SELECT CustomerId
    FROM Orders
)